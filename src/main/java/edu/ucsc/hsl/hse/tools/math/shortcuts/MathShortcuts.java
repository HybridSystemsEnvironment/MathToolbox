package edu.ucsc.hsl.hse.tools.math.shortcuts;

public class MathShortcuts
{

	public Double s(Double val)
	{
		return Math.sin(val);
	}

	public Double c(Double val)
	{
		return Math.cos(val);
	}

	public Double sin(Double val)
	{
		return Math.sin(val);
	}

	public Double cos(Double val)
	{
		return Math.cos(val);
	}

	public Double sq(Double val)
	{
		return Math.pow(val, 2);
	}

	public Double cu(Double val)
	{
		return Math.pow(val, 3);
	}
}
